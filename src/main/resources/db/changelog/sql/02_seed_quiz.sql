-- UUID конструируем через md5(text)::uuid, чтобы избежать литералов с длинными нулями.

WITH new_quiz AS (
    INSERT INTO quizzes (id, title, description)
    VALUES (md5('hb-default-quiz-vanya')::uuid,
            'День рождения Вани',
            'Сколько ты знаешь про главного авиамана МАИ?')
    RETURNING id
)
SELECT 1;

CREATE OR REPLACE FUNCTION seed_q(
    p_order INT,
    p_text TEXT,
    p_type TEXT,
    p_points INT,
    p_comment TEXT,
    p_options JSONB
) RETURNS VOID AS $$
DECLARE
    qid UUID := gen_random_uuid();
    quiz_uuid UUID := md5('hb-default-quiz-vanya')::uuid;
    opt JSONB;
BEGIN
    INSERT INTO questions (id, quiz_id, text, type, difficulty, points, after_answer_comment, order_number)
    VALUES (qid, quiz_uuid, p_text, p_type, 'medium', p_points, p_comment, p_order);

    FOR opt IN SELECT * FROM jsonb_array_elements(p_options)
    LOOP
        INSERT INTO options (id, question_id, option_key, text, is_correct)
        VALUES (gen_random_uuid(), qid,
                opt ->> 'k', opt ->> 't', (opt ->> 'c')::BOOLEAN);
    END LOOP;
END;
$$ LANGUAGE plpgsql;

